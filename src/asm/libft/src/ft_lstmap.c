#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list *add;
	t_list *list;

	list = f(lst);
	add = list;
	if (lst == NULL)
		return (NULL);
	list = f(lst);
	add = list;
	while (lst->next)
	{
		lst = lst->next;
		if (!(list->next = f(lst)))
		{
			free(list->next);
			return (NULL);
		}
		list = list->next;
	}
	return (add);
}
